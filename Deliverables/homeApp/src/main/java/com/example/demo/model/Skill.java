package com.example.demo.model;

public class Skill {
	private String name;
	private String image;
	
	public Skill(String name,String image) {
		this.name = name;
		this.image = image;
	}
	
	public String getName() {
		return name;
	}
	
	public String getImage(){
		return image;
	}
	
}

